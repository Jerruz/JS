function mySum(a, b){
    return a + b
}

function myDivide(a, b){
    return a / b
}

function myDiff(a, b){
    return a - b
}

function myProduct(a, b){
    return a * b
}

console.log(myDiff(5, 3))
console.log(mySum(5, 3))
console.log(myDivide(5, 3))
console.log(myProduct(5, 3))